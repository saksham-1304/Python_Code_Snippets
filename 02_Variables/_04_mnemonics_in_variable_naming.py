# Mnemonics in Variable Naming

# Unclear
a = 10

# Clear(using mnemonic)
num_students = 10

# Less descriptive
x = 5

# More descriptive(using mnemonic)
num_students_in_class = 5

# Inconsistent
numStudents = 5
numberOfStudents = 5

# Consistent(using mnemonic)
num_student = 5

# Unclear abbreviation
num_stud = 5  # What does "stud" stand for?

# Clear(using mnenomic)
num_students = 5
