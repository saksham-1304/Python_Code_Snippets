# Comparison Operations

# Equal To (==)
x_equal = (5 == 5)

# Not Equal To (!=)
x_not_equal = (5 != 10)

# Greater Than (>)
x_greater_than = (10 > 5)

# Less Than (<)
x_less_than = (5 < 10)

# Greater Than or Equal To (>=)
x_greater_or_equal = (10 >= 10)

# Less Than or Equal To (<=)
x_less_or_equal = (5 <= 5)
