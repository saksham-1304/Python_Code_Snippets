# Bool value

# Non-zero integers evalute to True
print(bool(10))  # Output:True
print(bool(-5))  # Output:True
print(bool(1000))  # Output:True

# Zero evalutes to False
print(bool(0))  # Output:False

# Non-zero floats evaluate to True
print(bool(3.14))  # Output:True
print(bool(-2.5))  # Output:True

# Zero evalutes To False
print(bool(0.0))  # Output:False

# Non-empty strings evalute to True
print(bool("hello"))  # Output:True
print(bool("Python"))  # Output:True

# Empty string evalutes to False
print(bool(""))  # Output:False
