# Modify elements in a list

# Define a list
my_list = ["apple", "banana", "cherry"]

print(my_list)

# Modify an element in place
my_list[1] = "orange"

# Print the modified list
print(my_list)

'''
Output:
['apple', 'banana', 'cherry']
['apple', 'orange', 'cherry']
'''

