# Functions to add numbers


def add_numbers(x, y):
    """
    Add two numbers together.
    Args:
    x(int or float): The first number.
    y(int or float):The second number

    Returns:
    int or float:The sum of x and y.
    """
    result = x + y
    return result


# Calling the function
sum_result = add_numbers(3, 5)
print(sum_result)
# Output:8
